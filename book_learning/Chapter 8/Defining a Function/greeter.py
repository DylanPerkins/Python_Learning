# Section: Defining a Function

def greet_user():                       # Function definition, tells Python the name of the function and, if applicable, what kind of information the function needs to do its job
    """Display a simple greeting."""    # Docstring, describes what the function does
    print("Hello!")                     # Function body, code that the function executes

greet_user()  # Function call, tells Python to execute the code in the function
